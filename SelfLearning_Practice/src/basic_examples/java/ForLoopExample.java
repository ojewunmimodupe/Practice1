
// This is an example on for loop

package basic_examples.java;

public class ForLoopExample {
	
	public static void main(String args []) 
	
	{
		int count; // Declaration of variable
		
		for(count = 0; count<5; count = count++); //starting number, number of iteration, steps----> this will iterate 5 times
		
			System.out.println("This is count " + count); // print the number of iteration
		System.out.println();
		System.out.println("Done");
		
		
		

		
	}

}
