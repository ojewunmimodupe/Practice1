
//This program converts gallons to liters

package basic_examples.java;

public class GallonToLiter {
	public static void main(String args []) 
	{
		
		double gallons; // Declaration of variable
		double liters;// Declaration of variable
		
		gallons = 10; //Assignment of value to variable
		
		liters = 10*3.7854;
		
		System.out.println(gallons + " gallons is " + liters + " liters");
	}

}
